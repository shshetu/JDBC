    private void jButtonSearchByIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchByIdActionPerformed
        int id = Integer.parseInt(jTextFieldSearchById.getText().trim());
        clearTable();
        StudentDao st = new StudentDaoImp();
        DefaultTableModel model = (DefaultTableModel) jTableStudentView.getModel();
        try {
            List<Student> list = st.findStudentById(id);
        } catch (Exception e) {
            List<Student> list = st.findStudentById(id);
            Object[] cols = new Object[2];
            for (int i = 0; i < list.size(); i++) {
                cols[0] = list.get(i).getId();
                cols[1] = list.get(i).getName();
                model.addRow(cols);
            }
        }

    }//GEN-LAST:event_jButtonSearchByIdActionPerformed

