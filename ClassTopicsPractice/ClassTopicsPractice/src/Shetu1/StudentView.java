    public void displayDataIntoTable() {
        clearTable();
        StudentDao st = new StudentDaoImp();
        DefaultTableModel model = (DefaultTableModel) jTableStudentView.getModel();
        List<Student> list = st.findStudent();
        Object[] cols = new Object[2];
        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getId();
            cols[1] = list.get(i).getName();
            model.addRow(cols);
        }

    }

    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) jTableStudentView.getModel();
        model.setRowCount(0);
    }

