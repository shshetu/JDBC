    private void jTableStudentViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableStudentViewMouseClicked
        DefaultTableModel model = (DefaultTableModel) jTableStudentView.getModel();
        int i = jTableStudentView.getSelectedRow();
        jTextFieldId.setText(model.getValueAt(i, 0).toString());
        jTextFieldName.setText(model.getValueAt(i, 1).toString());
        selectedId = Integer.parseInt(model.getValueAt(i, 0).toString());
    }//GEN-LAST:event_jTableStudentViewMouseClicked

