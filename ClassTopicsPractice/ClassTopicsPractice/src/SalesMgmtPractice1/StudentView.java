    public void displayStudentListIntoTable(){
    clearTable();
    //Create a list to add to the table
    List<Student> list = new StudentDaoImp().getStudentDataFromDatabaseTable();
    //create Table object
    DefaultTableModel model = (DefaultTableModel) jTableDisplay.getModel();
    Object cols[] = new Object[2];
        for (int  i = 0;  i < list.size();  i++) {
            cols[0] = list.get(i).getId();
            cols[1] = list.get(i).getName();
            model.addRow(cols);
        }
    
    }
