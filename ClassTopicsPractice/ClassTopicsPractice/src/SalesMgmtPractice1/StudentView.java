    private void jButtonInsert3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsert3ActionPerformed
        //Collect data from where in the form
        String stuViewCollectedData = jTextFieldName.getText().trim();
        //Create object of StudentDaoImp()
        StudentDao obj = new StudentDaoImp();

        //validate
        if(stuViewCollectedData.length()<4){
            System.out.println("Enter At least 4 characters!");
        }else{
            try {
                //FirstCheck duplicate data
                Student sDuplicate = obj.getStudentDataByName(stuViewCollectedData);
                //check
                if(sDuplicate.getName() != null){
                    JOptionPane.showMessageDialog(null, "Name already exists!");
                }else{
                //Send the value to the insert method
                //and thats why we need student type objec
               new StudentDaoImp().insert(new Student(stuViewCollectedData));
               displayStudentListIntoTable();
               JOptionPane.showMessageDialog(null, "Data successfully inserted into table!");
                
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButtonInsert3ActionPerformed

